CREATE TABLE ATTORE (
  ID_ATTORE VARCHAR(255),
  NOME_COMPLETO VARCHAR(255),
  PRIMARY KEY (ID_ATTORE)
);

CREATE TABLE FILM (
  ID_FILM VARCHAR(255),
  FK_REGISTA VARCHAR(255),
  PRIMARY KEY (ID_FILM, FK_REGISTA)
);

CREATE TABLE REGISTA (
  ID_REGISTA VARCHAR(255),
  NOME_COMPLETO VARCHAR(255),
  PRIMARY KEY (ID_REGISTA)
);

CREATE TABLE CINEMA (
  ID_CINEMA VARCHAR(255),
  PRIMARY KEY (ID_CINEMA)
);