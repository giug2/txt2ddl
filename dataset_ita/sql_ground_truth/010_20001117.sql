CREATE TABLE CASA (
  NOME VARCHAR(255),
  INDIRIZZO VARCHAR(255),
  PRIMARY KEY (NOME)
);

CREATE TABLE MODELLO (
  FK_CASA VARCHAR(255),
  NOME VARCHAR(255),
  ANNO VARCHAR(255),
  PRIMARY KEY (FK_CASA, NOME)
);

CREATE TABLE VERSIONE (
  FK_MODELLO VARCHAR(255),
  NOME VARCHAR(255),
  PREZZO VARCHAR(255),
  PORTE VARCHAR(255),
  VELOCITÀ VARCHAR(255),
  PRIMARY KEY (FK_MODELLO, NOME)
);

CREATE TABLE SEGMENTO (
  CODICE VARCHAR(255),
  DESCRIZIONE VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE MOTORE (
  CODICE VARCHAR(255),
  CILINDRATA VARCHAR(255),
  CILINDRI VARCHAR(255),
  POTENZA VARCHAR(255),
  PRIMARY KEY (CODICE)
);

