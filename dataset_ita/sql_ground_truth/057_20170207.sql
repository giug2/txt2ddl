CREATE TABLE COMMITTENTE (
  ID_COMMITTENTE VARCHAR(255),
  PRIMARY KEY (ID_COMMITTENTE)
);

CREATE TABLE IMPIEGATO (
  ID_IMPIEGATO VARCHAR(255),
  FK_DIPARTIMENTO VARCHAR(255),
  PRIMARY KEY (ID_IMPIEGATO, FK_DIPARTIMENTO)
);

CREATE TABLE PROGETTO (
  ID_PROGETTO VARCHAR(255),
  FK_COMMITTENTE VARCHAR(255),
  PRIMARY KEY (ID_PROGETTO, FK_COMMITTENTE)
);

CREATE TABLE DIPARTIMENTO (
  ID_DIPARTIMENTO VARCHAR(255),
  PRIMARY KEY (ID_DIPARTIMENTO)
);