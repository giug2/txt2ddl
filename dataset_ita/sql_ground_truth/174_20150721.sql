CREATE TABLE STUDENTE (
  MATRICOLA VARCHAR(255),
  COGNOME VARCHAR(255),
  NOME VARCHAR(255),
  DATA_DI_NASCITA VARCHAR(255),
  PRIMARY KEY (MATRICOLA)
);

CREATE TABLE ISCRIZIONECORSO (
  ID_ISCRIZIONE VARCHAR(255),
  ANNOACCADEMICO VARCHAR(255),
  PRIMARY KEY (ID_ISCRIZIONE)
);

CREATE TABLE TASSA (
  ID_TASSA VARCHAR(255),
  DATAEMISSIONE VARCHAR(255),
  IMPORTO VARCHAR(255),
  PRIMARY KEY (ID_TASSA)
);

CREATE TABLE PAGAMENTO (
  ID_PAGAMENTO VARCHAR(255),
  DATAPAGAMENTO VARCHAR(255),
  IMPORTO VARCHAR(255),
  PRIMARY KEY (ID_PAGAMENTO)
);

CREATE TABLE CORSODILAUREA (
  CODICECORSO VARCHAR(255),
  NOMECORSO VARCHAR(255),
  PRIMARY KEY (CODICECORSO)
);

