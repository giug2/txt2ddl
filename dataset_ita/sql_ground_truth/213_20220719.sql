CREATE TABLE IMPIEGATO (
  ID_IMPIEGATO VARCHAR(255),
  FK_ID_DIVISIONE VARCHAR(255),
  NOME VARCHAR(255),
  COGNOME VARCHAR(255),
  PRIMARY KEY (ID_IMPIEGATO, FK_ID_DIVISIONE)
);

CREATE TABLE DIVISIONE (
  ID_DIVISIONE VARCHAR(255),
  FK_ID_AZIENDA VARCHAR(255),
  NOME_DIVISIONE VARCHAR(255),
  PRIMARY KEY (ID_DIVISIONE, FK_ID_AZIENDA)
);

CREATE TABLE PROGETTO (
  ID_PROGETTO VARCHAR(255),
  FK_ID_DIVISIONE VARCHAR(255),
  NOME_PROGETTO VARCHAR(255),
  PRIMARY KEY (ID_PROGETTO, FK_ID_DIVISIONE)
);

CREATE TABLE AZIENDA (
  ID_AZIENDA VARCHAR(255),
  NOME_AZIENDA VARCHAR(255),
  PRIMARY KEY (ID_AZIENDA)
);