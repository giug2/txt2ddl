CREATE TABLE SCAFFALE (
  CODICESCAFFALE VARCHAR(255),
  PRIMARY KEY (CODICESCAFFALE)
);

CREATE TABLE MAGAZZINO (
  CODICEMAGAZZINO VARCHAR(255),
  INDIRIZZO VARCHAR(255),
  PRIMARY KEY (CODICEMAGAZZINO)
);

CREATE TABLE PRODOTTO (
  SKU VARCHAR(255),
  PRIMARY KEY (SKU)
);

CREATE TABLE INVENTARIO (
  DATAINVENTARIO VARCHAR(255),
  ARTICOLI_CONTATI VARCHAR(255),
  PRIMARY KEY (DATAINVENTARIO)
);

