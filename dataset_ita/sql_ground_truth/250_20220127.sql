CREATE TABLE PROFESSORE (
  ID_PROFESSORE VARCHAR(255),
  PRIMARY KEY (ID_PROFESSORE)
);

CREATE TABLE CORSO (
  ID_CORSO VARCHAR(255),
  FK_DIPARTIMENTO VARCHAR(255),
  PRIMARY KEY (ID_CORSO, FK_DIPARTIMENTO)
);

CREATE TABLE TUTOR (
  ID_TUTOR VARCHAR(255),
  PRIMARY KEY (ID_TUTOR)
);

CREATE TABLE DIPARTIMENTO (
  ID_DIPARTIMENTO VARCHAR(255),
  PRIMARY KEY (ID_DIPARTIMENTO)
);

CREATE TABLE REL_PROFESSORE_TUTOR_CORSO (
  FK_PROFESSORE VARCHAR(255),
  FK_TUTOR VARCHAR(255),
  FK_CORSO VARCHAR(255),
  PRIMARY KEY (FK_PROFESSORE, FK_TUTOR, FK_CORSO),
  FOREIGN KEY (FK_PROFESSORE) REFERENCES PROFESSORE,
  FOREIGN KEY (FK_TUTOR) REFERENCES TUTOR,
  FOREIGN KEY (FK_CORSO) REFERENCES CORSO
);