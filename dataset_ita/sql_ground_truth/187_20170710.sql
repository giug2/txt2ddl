  ID_ARTISTA VARCHAR(255),
  COGNOME VARCHAR(255),
  NOME VARCHAR(255),
  DATA_NASCITA VARCHAR(255),
  PRIMARY KEY (ID_ARTISTA)
);

CREATE TABLE STUDIO_PRODUZIONE (
  ID_STUDIO VARCHAR(255),
  NOME VARCHAR(255),
  PRIMARY KEY (ID_STUDIO)
);

