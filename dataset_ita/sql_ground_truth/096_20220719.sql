CREATE TABLE DOCENTE (
  ID_DOCENTE VARCHAR(255),
  FK_ID_DIPARTIMENTO VARCHAR(255),
  NOME VARCHAR(255),
  COGNOME VARCHAR(255),
  PRIMARY KEY (ID_DOCENTE, FK_ID_DIPARTIMENTO)
);

CREATE TABLE DIPARTIMENTO (
  ID_DIPARTIMENTO VARCHAR(255),
  FK_ID_FACOLTA VARCHAR(255),
  NOME_DIPARTIMENTO VARCHAR(255),
  PRIMARY KEY (ID_DIPARTIMENTO, FK_ID_FACOLTA)
);

CREATE TABLE CORSO_DI_STUDIO (
  ID_CORSO_STUDIO VARCHAR(255),
  FK_ID_DIPARTIMENTO VARCHAR(255),
  NOME_CORSO VARCHAR(255),
  PRIMARY KEY (ID_CORSO_STUDIO, FK_ID_DIPARTIMENTO)
);

CREATE TABLE FACOLTÀ (
  ID_FACOLTA VARCHAR(255),
  NOME_FACOLTA VARCHAR(255),
  PRIMARY KEY (ID_FACOLTA)
);