CREATE TABLE DIRIGENTE (
  ID_DIRIGENTE VARCHAR(255),
  PRIMARY KEY (ID_DIRIGENTE)
);

CREATE TABLE UFFICIO (
  ID_UFFICIO VARCHAR(255),
  FK_SEDE VARCHAR(255),
  PRIMARY KEY (ID_UFFICIO, FK_SEDE)
);

CREATE TABLE ASSISTENTE (
  ID_ASSISTENTE VARCHAR(255),
  PRIMARY KEY (ID_ASSISTENTE)
);

CREATE TABLE SEDE (
  ID_SEDE VARCHAR(255),
  PRIMARY KEY (ID_SEDE)
);

CREATE TABLE REL_DIRIGENTE_ASSISTENTE_UFFICIO (
  FK_DIRIGENTE VARCHAR(255),
  FK_ASSISTENTE VARCHAR(255),
  FK_UFFICIO VARCHAR(255),
  PRIMARY KEY (FK_DIRIGENTE, FK_ASSISTENTE, FK_UFFICIO),
  FOREIGN KEY (FK_DIRIGENTE) REFERENCES DIRIGENTE,
  FOREIGN KEY (FK_ASSISTENTE) REFERENCES ASSISTENTE,
  FOREIGN KEY (FK_UFFICIO) REFERENCES UFFICIO
);