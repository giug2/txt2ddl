);

CREATE TABLE STUDENTE (
  MATRICOLA_STUDENTE VARCHAR(255),
  FK_CORSO_LAUREA VARCHAR(255),
  PRIMARY KEY (MATRICOLA_STUDENTE, FK_CORSO_LAUREA)
);

