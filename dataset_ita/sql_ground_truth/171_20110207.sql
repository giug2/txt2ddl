CREATE TABLE LIBRO (
  ISBN VARCHAR(255),
  TITOLO VARCHAR(255),
  COPIEDISPONIBILI VARCHAR(255),
  ANNOPUBBLICAZIONE VARCHAR(255),
  PRIMARY KEY (ISBN)
);

CREATE TABLE GENERE (
  IDGENERE VARCHAR(255),
  NOMEGENERE VARCHAR(255),
  PRIMARY KEY (IDGENERE)
);

CREATE TABLE EDITORE (
  IDEDITORE VARCHAR(255),
  NOMEEDITORE VARCHAR(255),
  PRIMARY KEY (IDEDITORE)
);

CREATE TABLE PRESTITO (
  IDPRESTITO VARCHAR(255),
  DATAPRESTITO VARCHAR(255),
  STATO VARCHAR(255),
  PRIMARY KEY (IDPRESTITO)
);

CREATE TABLE UTENTE (
  TESSERA VARCHAR(255),
  NOMECOMPLETO VARCHAR(255),
  INDIRIZZO VARCHAR(255),
  PRIMARY KEY (TESSERA)
);