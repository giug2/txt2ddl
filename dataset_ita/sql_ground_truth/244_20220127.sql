CREATE TABLE ARTISTA (
  ID_ARTISTA VARCHAR(255),
  PRIMARY KEY (ID_ARTISTA)
);

CREATE TABLE ALBUM (
  ID_ALBUM VARCHAR(255),
  FK_GENERE VARCHAR(255),
  PRIMARY KEY (ID_ALBUM, FK_GENERE)
);

CREATE TABLE CANZONE (
  ID_CANZONE VARCHAR(255),
  FK_ALBUM VARCHAR(255),
  PRIMARY KEY (ID_CANZONE, FK_ALBUM)
);

CREATE TABLE GENERE (
  ID_GENERE VARCHAR(255),
  PRIMARY KEY (ID_GENERE)
);