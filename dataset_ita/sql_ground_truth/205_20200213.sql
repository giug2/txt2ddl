CREATE TABLE DIPENDENTE (
  ID_DIPENDENTE VARCHAR(255),
  FK_DIVISIONE VARCHAR(255),
  PRIMARY KEY (ID_DIPENDENTE, FK_DIVISIONE)
);

CREATE TABLE DIVISIONE (
  ID_DIVISIONE VARCHAR(255),
  FK_AZIENDA VARCHAR(255),
  PRIMARY KEY (ID_DIVISIONE, FK_AZIENDA)
);

CREATE TABLE PROGETTO (
  ID_PROGETTO VARCHAR(255),
  FK_DIVISIONE VARCHAR(255),
  PRIMARY KEY (ID_PROGETTO, FK_DIVISIONE)
);

CREATE TABLE AZIENDA (
  ID_AZIENDA VARCHAR(255),
  PRIMARY KEY (ID_AZIENDA)
);