CREATE TABLE DOCENTE (
  ID_DOCENTE VARCHAR(255),
  PRIMARY KEY (ID_DOCENTE)
);

CREATE TABLE CORSO (
  ID_CORSO VARCHAR(255),
  FK_DOCENTE VARCHAR(255),
  PRIMARY KEY (ID_CORSO, FK_DOCENTE)
);

