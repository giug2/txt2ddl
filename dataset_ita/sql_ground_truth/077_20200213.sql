CREATE TABLE PROFESSORE (
  ID_PROFESSORE VARCHAR(255),
  FK_SCUOLA VARCHAR(255),
  FK_DIPARTIMENTO VARCHAR(255),
  NOME VARCHAR(255),
  COGNOME VARCHAR(255),
  PRIMARY KEY (ID_PROFESSORE, FK_SCUOLA, FK_DIPARTIMENTO)
);

CREATE TABLE CORSO_DI_STUDIO (
  ID_CORSO_DI_STUDIO VARCHAR(255),
  FK_SCUOLA VARCHAR(255),
  NOME_CORSO VARCHAR(255),
  PRIMARY KEY (ID_CORSO_DI_STUDIO, FK_SCUOLA)
);

CREATE TABLE DIPARTIMENTO (
  ID_DIPARTIMENTO VARCHAR(255),
  NOME_DIPARTIMENTO VARCHAR(255),
  PRIMARY KEY (ID_DIPARTIMENTO)
);

CREATE TABLE SCUOLA (
  ID_SCUOLA VARCHAR(255),
  NOME_SCUOLA VARCHAR(255),
  PRIMARY KEY (ID_SCUOLA)
);