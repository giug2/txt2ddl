CREATE TABLE COPIA_MAGAZZINO (
  ID_COPIA VARCHAR(255),
  STATO_CONSERVAZIONE VARCHAR(255),
  DATA_ACQUISIZIONE VARCHAR(255),
  PRIMARY KEY (ID_COPIA)
);

CREATE TABLE EDIZIONE (
  ISBN VARCHAR(255),
  TITOLO_EDIZIONE VARCHAR(255),
  FORMATO VARCHAR(255),
  PREZZO_COPERTINA VARCHAR(255),
  PRIMARY KEY (ISBN)
);

CREATE TABLE LINGUA (
  CODICE_ISO VARCHAR(255),
  NOME_LINGUA VARCHAR(255),
  PRIMARY KEY (CODICE_ISO)
);

CREATE TABLE OPERA_LETTERARIA (
  ID_OPERA VARCHAR(255),
  NUMERO_PAGINE_MEDIO VARCHAR(255),
  NUMERO_VOLUMI VARCHAR(255),
  PRIMARY KEY (ID_OPERA)
);

