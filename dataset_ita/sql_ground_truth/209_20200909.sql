CREATE TABLE DOCENTE (
  MATRICOLA VARCHAR(255),
  COGNOME VARCHAR(255),
  NOME VARCHAR(255),
  PRIMARY KEY (MATRICOLA)
);

CREATE TABLE INSEGNAMENTO_EROGATO (
  FK_CORSO_DI_STUDIO_CODICE VARCHAR(255),
  FK_DIPARTIMENTO_CODICE VARCHAR(255),
  ANNO_ACCADEMICO VARCHAR(255),
  PRIMARY KEY (FK_CORSO_DI_STUDIO_CODICE, FK_DIPARTIMENTO_CODICE)
);

CREATE TABLE CORSO_DI_STUDIO (
  CODICE VARCHAR(255),
  NOME VARCHAR(255),
  ANNO_FONDAZIONE VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE COORDINATORE (
  ID VARCHAR(255),
  COGNOME VARCHAR(255),
  NOME VARCHAR(255),
  DATA_INIZIO_INCARICO VARCHAR(255),
  DATA_FINE_INCARICO VARCHAR(255),
  PRIMARY KEY (ID)
);

CREATE TABLE DIPARTIMENTO (
  CODICE VARCHAR(255),
  NOME VARCHAR(255),
  SEDE VARCHAR(255),
  CONTATTO VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE QUOTA_CONTRIBUTO (
  FK_INSEGNAMENTO_EROGATO_CORSO VARCHAR(255),
  FK_INSEGNAMENTO_EROGATO_DIPARTIMENTO VARCHAR(255),
  FK_FASCIA_REDDITO_CODICE VARCHAR(255),
  IMPORTO VARCHAR(255),
  PRIMARY KEY (FK_INSEGNAMENTO_EROGATO_CORSO, FK_INSEGNAMENTO_EROGATO_DIPARTIMENTO, FK_FASCIA_REDDITO_CODICE)
);

CREATE TABLE FASCIA_REDDITO (
  CODICE VARCHAR(255),
  DESCRIZIONE VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE TIPO_ESONERO (
  CODICE VARCHAR(255),
