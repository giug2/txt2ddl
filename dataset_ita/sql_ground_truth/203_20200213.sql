CREATE TABLE SVILUPPATORE (
  ID_SVILUPPATORE VARCHAR(255),
  FK_AZIENDA VARCHAR(255),
  FK_TEAM VARCHAR(255),
  NOME VARCHAR(255),
  COGNOME VARCHAR(255),
  PRIMARY KEY (ID_SVILUPPATORE, FK_AZIENDA, FK_TEAM)
);

CREATE TABLE PROGETTO (
  ID_PROGETTO VARCHAR(255),
  FK_AZIENDA VARCHAR(255),
  NOME_PROGETTO VARCHAR(255),
  PRIMARY KEY (ID_PROGETTO, FK_AZIENDA)
);

CREATE TABLE TEAM (
  ID_TEAM VARCHAR(255),
  NOME_TEAM VARCHAR(255),
  PRIMARY KEY (ID_TEAM)
);

CREATE TABLE AZIENDA (
  ID_AZIENDA VARCHAR(255),
  NOME_AZIENDA VARCHAR(255),
  PRIMARY KEY (ID_AZIENDA)
);