  PRIMARY KEY (CODICE)
);

CREATE TABLE MARCA (
  CODICE VARCHAR(255),
  NOME_MARCA VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE CATEGORIA (
  CODICE VARCHAR(255),
  NOME_CATEGORIA VARCHAR(255),
  PRIMARY KEY (CODICE)
);

