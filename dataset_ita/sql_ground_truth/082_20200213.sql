CREATE TABLE DOCENTE (
  ID_DOCENTE VARCHAR(255),
  FK_CORSO VARCHAR(255),
  PRIMARY KEY (ID_DOCENTE, FK_CORSO)
);

CREATE TABLE CORSO_DI_STUDIO (
  ID_CORSO_DI_STUDIO VARCHAR(255),
  FK_DIPARTIMENTO VARCHAR(255),
  PRIMARY KEY (ID_CORSO_DI_STUDIO, FK_DIPARTIMENTO)
);

CREATE TABLE DIPARTIMENTO (
  ID_DIPARTIMENTO VARCHAR(255),
  FK_FACOLTA VARCHAR(255),
  PRIMARY KEY (ID_DIPARTIMENTO, FK_FACOLTA)
);

CREATE TABLE FACOLTÀ (
  ID_FACOLTA VARCHAR(255),
  PRIMARY KEY (ID_FACOLTA)
);