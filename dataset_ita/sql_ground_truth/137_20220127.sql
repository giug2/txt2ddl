CREATE TABLE MANAGER (
  ID_MANAGER VARCHAR(255),
  PRIMARY KEY (ID_MANAGER)
);

CREATE TABLE REPARTO (
  ID_REPARTO VARCHAR(255),
  FK_SEDE VARCHAR(255),
  PRIMARY KEY (ID_REPARTO, FK_SEDE)
);

CREATE TABLE ASSISTENTE (
  ID_ASSISTENTE VARCHAR(255),
  FK_REPARTO VARCHAR(255),
  PRIMARY KEY (ID_ASSISTENTE, FK_REPARTO)
);

CREATE TABLE SEDE (
  ID_SEDE VARCHAR(255),
  PRIMARY KEY (ID_SEDE)
);