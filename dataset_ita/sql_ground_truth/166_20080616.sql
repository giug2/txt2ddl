CREATE TABLE STUDENTE (
  MATRICOLA VARCHAR(255),
  NOME VARCHAR(255),
  COGNOME VARCHAR(255),
  TELEFONO VARCHAR(255),
  PRIMARY KEY (MATRICOLA)
);

CREATE TABLE PIANO_DI_STUDI (
  ID_PIANO VARCHAR(255),
  PRIMARY KEY (ID_PIANO)
);

CREATE TABLE CORSO_NEL_PIANO (
  ID_RIGA VARCHAR(255),
  FK_PIANO_DI_STUDI VARCHAR(255),
  PRIMARY KEY (ID_RIGA, FK_PIANO_DI_STUDI)
);

CREATE TABLE TIPOLOGIA (
  CODICE VARCHAR(255),
  DESCRIZIONE VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE SESSIONE_DI_CORSO (
  ORARIO VARCHAR(255),
  FK_INSEGNAMENTO VARCHAR(255),
  GIORNO_SETTIMANA VARCHAR(255),
  PRIMARY KEY (ORARIO, FK_INSEGNAMENTO, GIORNO_SETTIMANA)
);

