CREATE TABLE INFERMIERE (
  ID_INFERMIERE VARCHAR(255),
  NOME VARCHAR(255),
  COGNOME VARCHAR(255),
  PRIMARY KEY (ID_INFERMIERE)
);

CREATE TABLE DOTTORE (
  ID_DOTTORE VARCHAR(255),
  NOME VARCHAR(255),
  COGNOME VARCHAR(255),
  PRIMARY KEY (ID_DOTTORE)
);

CREATE TABLE OSPEDALE (
  ID_OSPEDALE VARCHAR(255),
  INDIRIZZO VARCHAR(255),
  CITTA VARCHAR(255),
  PRIMARY KEY (ID_OSPEDALE)
);

CREATE TABLE REPARTO (
  ID_REPARTO VARCHAR(255),
  FK_OSPEDALE VARCHAR(255),
  NOME_REPARTO VARCHAR(255),
  PIANO VARCHAR(255),
  PRIMARY KEY (ID_REPARTO, FK_OSPEDALE)
);

CREATE TABLE REL_DOTTORE_INFERMIRE_REPARTO (
  FK_DOTTORE VARCHAR(255),
  FK_INFERMIRE VARCHAR(255),
  FK_REPARTO VARCHAR(255),
  PRIMARY KEY (FK_DOTTORE, FK_INFERMIRE, FK_REPARTO),
  FOREIGN KEY (FK_DOTTORE) REFERENCES DOTTORE,
  FOREIGN KEY (FK_INFERMIRE) REFERENCES INFERMIRE,
  FOREIGN KEY (FK_REPARTO) REFERENCES REPARTO
);