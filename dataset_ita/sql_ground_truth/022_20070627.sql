CREATE TABLE ISTITUZIONE (
  CODICE VARCHAR(255),
  NOME VARCHAR(255),
  INDIRIZZO VARCHAR(255),
  NAZIONE VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE AUTORE (
  CODICE VARCHAR(255),
  COGNOME VARCHAR(255),
  NOME VARCHAR(255),
  E-MAIL VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE ARTICOLO (
  CODICE VARCHAR(255),
  TITOLO VARCHAR(255),
  SOTTOTITOLO VARCHAR(255),
  TESTO VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE PUBBLICAZIONE (
  NUMERO VARCHAR(255),
  FK_VOLUME VARCHAR(255),
  DATA VARCHAR(255),
  PRIMARY KEY (NUMERO, FK_VOLUME)
);

CREATE TABLE VOLUME (
  ANNO VARCHAR(255),
  TITOLO VARCHAR(255),
  PRIMARY KEY (ANNO)
);