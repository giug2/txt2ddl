CREATE TABLE ETICHETTADISCOGRAFICA (
  ID_ETICHETTA VARCHAR(255),
  NOME VARCHAR(255),
  SEDELEGALE VARCHAR(255),
  PAESE VARCHAR(255),
  PRIMARY KEY (ID_ETICHETTA)
);

CREATE TABLE ARTISTA (
  ID_ARTISTA VARCHAR(255),
  COGNOME VARCHAR(255),
  NOMEARTE VARCHAR(255),
  EMAILCONTATTO VARCHAR(255),
  PRIMARY KEY (ID_ARTISTA)
);

CREATE TABLE BRANO (
  ID_BRANO VARCHAR(255),
  TITOLO VARCHAR(255),
  FEATURING VARCHAR(255),
  TESTO VARCHAR(255),
  PRIMARY KEY (ID_BRANO)
);

CREATE TABLE ALBUM (
  NUMEROCATALOGO VARCHAR(255),
  FK_SERIE VARCHAR(255),
  DATAUSCITA VARCHAR(255),
  PRIMARY KEY (NUMEROCATALOGO, FK_SERIE)
);

CREATE TABLE SERIE (
  ANNOINIZIO VARCHAR(255),
  NOMESERIE VARCHAR(255),
  PRIMARY KEY (ANNOINIZIO)
);