CREATE TABLE STUDENTE (
  ID_STUDENTE VARCHAR(255),
  NOME_COMPLETO VARCHAR(255),
  PRIMARY KEY (ID_STUDENTE)
);

CREATE TABLE CORSO (
  ID_CORSO VARCHAR(255),
  FK_DOCENTE VARCHAR(255),
  PRIMARY KEY (ID_CORSO, FK_DOCENTE)
);

CREATE TABLE DOCENTE (
  ID_DOCENTE VARCHAR(255),
  NOME_COMPLETO VARCHAR(255),
  PRIMARY KEY (ID_DOCENTE)
);

CREATE TABLE AULA (
  ID_AULA VARCHAR(255),
  PRIMARY KEY (ID_AULA)
);