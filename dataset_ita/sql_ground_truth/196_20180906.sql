CREATE TABLE PAZIENTE (
  CODICEFISCALE VARCHAR(255),
  COGNOME VARCHAR(255),
  NOME VARCHAR(255),
  DATANASCITA VARCHAR(255),
  PRIMARY KEY (CODICEFISCALE)
);

CREATE TABLE RICOVERO (
  ID_RICOVERO VARCHAR(255),
  REPARTO VARCHAR(255),
  PRIMARY KEY (ID_RICOVERO)
);

CREATE TABLE FATTURA (
  NUMEROFATTURA VARCHAR(255),
  DATAEMISSIONE VARCHAR(255),
  IMPORTO VARCHAR(255),
  PRIMARY KEY (NUMEROFATTURA)
);

CREATE TABLE PAGAMENTO (
  ID_TRANSAZIONE VARCHAR(255),
  DATAPAGAMENTO VARCHAR(255),
  IMPORTOVERSATO VARCHAR(255),
  PRIMARY KEY (ID_TRANSAZIONE)
);

CREATE TABLE OSPEDALE (
  CODICEOSPEDALE VARCHAR(255),
  NOMESTRUTTURA VARCHAR(255),
  PRIMARY KEY (CODICEOSPEDALE)
);

CREATE TABLE ASL (
  CODICEASL VARCHAR(255),
  DENOMINAZIONE VARCHAR(255),
  PRIMARY KEY (CODICEASL)
);

