CREATE TABLE COLLEGIO (
  NUMERO VARCHAR(255),
  PRIMARY KEY (NUMERO)
);

CREATE TABLE REGIONE (
  CODICE VARCHAR(255),
  NOME VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE ELETTO (
  CODICE VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE ELEZIONE (
  ANNO VARCHAR(255),
  ELETTORI VARCHAR(255),
  PRIMARY KEY (ANNO)
);

CREATE TABLE PERSONA (
  CODICE VARCHAR(255),
  PRIMARY KEY (CODICE)
);