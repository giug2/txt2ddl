CREATE TABLE PRODOTTO (
  CODICE VARCHAR(255),
  NOME VARCHAR(255),
  QUANTITÀ_DISPONIBILE VARCHAR(255),
  PREZZO VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE CATEGORIA (
  CODICE VARCHAR(255),
  DESCRIZIONE VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE FORNITORE (
  CODICE VARCHAR(255),
  NOME VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE ORDINE (
  NUMERO VARCHAR(255),
  DATA VARCHAR(255),
  STATO VARCHAR(255),
  PRIMARY KEY (NUMERO)
);

CREATE TABLE CLIENTE (
  CODICE VARCHAR(255),
  NOME VARCHAR(255),
  INDIRIZZO VARCHAR(255),
  PRIMARY KEY (CODICE)
);