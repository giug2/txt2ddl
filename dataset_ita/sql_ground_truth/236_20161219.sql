  CODICE_ATENEO VARCHAR(255),
  NOME VARCHAR(255),
  PRIMARY KEY (CODICE_ATENEO)
);

CREATE TABLE REGIONE (
  ID VARCHAR(255),
  NOME VARCHAR(255),
  PRIMARY KEY (ID)
);

CREATE TABLE STATO (
  SIGLA VARCHAR(255),
  NOME VARCHAR(255),
  PRIMARY KEY (SIGLA)
);

