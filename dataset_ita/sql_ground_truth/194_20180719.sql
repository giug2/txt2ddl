CREATE TABLE UNIVERSITA (
  ID VARCHAR(255),
  NOME VARCHAR(255),
  PRIMARY KEY (ID)
);

CREATE TABLE CITTA (
  ID VARCHAR(255),
  NOME VARCHAR(255),
  PRIMARY KEY (ID)
);

CREATE TABLE REGIONE (
  SIGLA VARCHAR(255),
  NOME VARCHAR(255),
  PRIMARY KEY (SIGLA)
);

CREATE TABLE DIPARTIMENTO (
  NOME VARCHAR(255),
  FK_UNIVERSITA VARCHAR(255),
  NUMERO_UFFICI VARCHAR(255),
  PRIMARY KEY (NOME, FK_UNIVERSITA)
);

CREATE TABLE CORSO (
  CODICE VARCHAR(255),
  NOME_CORSO VARCHAR(255),
  ANNO_ACCADEMICO VARCHAR(255),
  PRIMARY KEY (CODICE)
);

CREATE TABLE PROFESSORE (
  MATRICOLA VARCHAR(255),
  NOME VARCHAR(255),
  COGNOME VARCHAR(255),
  PRIMARY KEY (MATRICOLA)
);

