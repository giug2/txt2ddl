CREATE TABLE MANAGER (
  ID_MANAGER VARCHAR(255),
  NOME_MANAGER VARCHAR(255),
  PRIMARY KEY (ID_MANAGER)
);

CREATE TABLE REPARTO (
  ID_REPARTO VARCHAR(255),
  NOME_REPARTO VARCHAR(255),
  PRIMARY KEY (ID_REPARTO)
);

CREATE TABLE ASSISTENTE (
  ID_ASSISTENTE VARCHAR(255),
  NOME_ASSISTENTE VARCHAR(255),
  PRIMARY KEY (ID_ASSISTENTE)
);

CREATE TABLE SEDE (
  ID_SEDE VARCHAR(255),
  INDIRIZZO_SEDE VARCHAR(255),
  PRIMARY KEY (ID_SEDE)
);

CREATE TABLE REL_MANAGER_ASSISTENTE_REPARTO (
  FK_MANAGER VARCHAR(255),
  FK_ASSISTENTE VARCHAR(255),
  FK_REPARTO VARCHAR(255),
  PRIMARY KEY (FK_MANAGER, FK_ASSISTENTE, FK_REPARTO),
  FOREIGN KEY (FK_MANAGER) REFERENCES MANAGER,
  FOREIGN KEY (FK_ASSISTENTE) REFERENCES ASSISTENTE,
  FOREIGN KEY (FK_REPARTO) REFERENCES REPARTO
);