CREATE TABLE UNIVERSITY (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  FOUNDATION VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE CITY (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  NATION VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE PROFESSOR (
  CODE VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  DATE_OF_BIRTH VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE QUALIFICATION (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

