CREATE TABLE RECORD_LABEL (
  LABEL_ID VARCHAR(255),
  NAME VARCHAR(255),
  SIT_LEGALLY VARCHAR(255),
  VILLAGE VARCHAR(255),
  PRIMARY KEY (LABEL_ID)
);

CREATE TABLE ARTIST (
  ARTIST_ID VARCHAR(255),
  SURNAME VARCHAR(255),
  NAMEART VARCHAR(255),
  EMAILCONTACT VARCHAR(255),
  PRIMARY KEY (ARTIST_ID)
);

CREATE TABLE SONG (
  TRACK_ID VARCHAR(255),
  TITLE VARCHAR(255),
  FEATURING VARCHAR(255),
  TEXT VARCHAR(255),
  PRIMARY KEY (TRACK_ID)
);

CREATE TABLE ALBUM (
  CATALOG_NUMBER VARCHAR(255),
  FK_SERIES VARCHAR(255),
  DATAUSCITA VARCHAR(255),
  PRIMARY KEY (CATALOG_NUMBER, FK_SERIES)
);

CREATE TABLE SERIE (
  ANNOINIZIO VARCHAR(255),
  NOMESERIE VARCHAR(255),
  PRIMARY KEY (ANNOINIZIO)
);