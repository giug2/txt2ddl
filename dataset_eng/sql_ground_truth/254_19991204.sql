  REGISTRATION_DATE VARCHAR(255),
  FK_EXAM_SESSION VARCHAR(255),
  PRIMARY KEY (REGISTRATION_DATE, FK_EXAM_SESSION)
);

CREATE TABLE SESSIONEESAME (
  SESSION_ID VARCHAR(255),
  ACADEMIC_YEAR VARCHAR(255),
  PRIMARY KEY (SESSION_ID)
);

CREATE TABLE TEACHING (
  TEACHINGCODE VARCHAR(255),
  FK_CORSO_LAUREA VARCHAR(255),
  CFU VARCHAR(255),
  PRIMARY KEY (TEACHINGCODE, FK_CORSO_LAUREA)
);

CREATE TABLE DEGREE_COURSE (
  COURSENAME VARCHAR(255),
  FK_DEPARTMENT VARCHAR(255),
  TIPOLAUREA VARCHAR(255),
  PRIMARY KEY (COURSENAME, FK_DEPARTMENT)
);

CREATE TABLE DEPARTMENT (
  DEPARTMENT_CODE VARCHAR(255),
  DEPARTMENTNAME VARCHAR(255),
  PRIMARY KEY (DEPARTMENT_CODE)
);

