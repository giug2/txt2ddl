CREATE TABLE TEACHING (
  TEACHER_ID VARCHAR(255),
  FK_DEPARTMENT_ID VARCHAR(255),
  NAME VARCHAR(255),
  SURNAME VARCHAR(255),
  PRIMARY KEY (TEACHER_ID, FK_DEPARTMENT_ID)
);

CREATE TABLE DEPARTMENT (
  DEPARTMENT_ID VARCHAR(255),
  FK_FACULTY_ID VARCHAR(255),
  DEPARTMENT_NAME VARCHAR(255),
  PRIMARY KEY (DEPARTMENT_ID, FK_FACULTY_ID)
);

CREATE TABLE COURSE_OF_STUDY (
  STUDY_COURSE_ID VARCHAR(255),
  FK_DEPARTMENT_ID VARCHAR(255),
  COURSE_NAME VARCHAR(255),
  PRIMARY KEY (STUDY_COURSE_ID, FK_DEPARTMENT_ID)
);

CREATE TABLE FACULTY (
  FACULTY_ID VARCHAR(255),
  FACULTY_NAME VARCHAR(255),
  PRIMARY KEY (FACULTY_ID)
);