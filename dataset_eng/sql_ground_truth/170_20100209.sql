CREATE TABLE VEHICLE (
  TARGA VARCHAR(255),
  OWNER VARCHAR(255),
  OWNER_ADDRESS VARCHAR(255),
  INITIAL_MILEAGE VARCHAR(255),
  CURRENT_MILEAGE VARCHAR(255),
  PRIMARY KEY (TARGA)
);

CREATE TABLE INTERVENTION (
  INTERVENTION_CODE VARCHAR(255),
  SURGERY_DATE VARCHAR(255),
  NOTE VARCHAR(255),
  COST VARCHAR(255),
  PRIMARY KEY (INTERVENTION_CODE)
);

CREATE TABLE TYPE_OF_INTERVENTION (
  TYPE_CODE VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (TYPE_CODE)
);

