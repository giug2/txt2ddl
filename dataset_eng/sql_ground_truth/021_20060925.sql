  DATAFINE VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE ITINERARY (
  CODE VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE EXPOSURE (
  FK_SHOW VARCHAR(255),
  FK_QUADRO VARCHAR(255),
  PRIMARY KEY (FK_SHOW, FK_QUADRO)
);

CREATE TABLE QUADRO (
  CODE VARCHAR(255),
  AUTHOR VARCHAR(255),
  ANNO VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE SALA (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  SIZE VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE TIPOVISITA (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  ORA VARCHAR(255),
  TERM VARCHAR(255),
  PRIMARY KEY (CODE)
);

