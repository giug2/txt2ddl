CREATE TABLE PROFESSOR (
  PROFESSOR_ID VARCHAR(255),
  PERSONAL_DATA VARCHAR(255),
  PRIMARY KEY (PROFESSOR_ID)
);

CREATE TABLE CORSO (
  CHANNEL VARCHAR(255),
  FK_COURSE_OF_STUDY VARCHAR(255),
  FK_MATERIA VARCHAR(255),
  COURSE_NAME VARCHAR(255),
  PRIMARY KEY (CHANNEL, FK_COURSE_OF_STUDY, FK_MATERIA)
);

CREATE TABLE COURSE_OF_STUDY (
  STUDY_COURSE_ID VARCHAR(255),
  NAME_COURSE_OF_STUDY VARCHAR(255),
  PRIMARY KEY (STUDY_COURSE_ID)
);

CREATE TABLE MATERIA (
  ID_MATERIA VARCHAR(255),
  MATTER_NAME VARCHAR(255),
  PRIMARY KEY (ID_MATERIA)
);

CREATE TABLE TEXT (
  TEXT_ID VARCHAR(255),
  TITLE VARCHAR(255),
  PRIMARY KEY (TEXT_ID)
