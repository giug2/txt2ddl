CREATE TABLE STUDENTS (
  FRESHMAN VARCHAR(255),
  NAME VARCHAR(255),
  SURNAME VARCHAR(255),
  TELEPHONE VARCHAR(255),
  PRIMARY KEY (FRESHMAN)
);

CREATE TABLE STUDY_PLAN (
  PLAN_ID VARCHAR(255),
  PRIMARY KEY (PLAN_ID)
);

CREATE TABLE COURSE_IN_THE_PLAN (
  ID_RIGA VARCHAR(255),
  FK_STUDY_PLAN VARCHAR(255),
  PRIMARY KEY (ID_RIGA, FK_STUDY_PLAN)
);

CREATE TABLE TYPOLOGY (
  CODE VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE COURSE_SESSION (
  HOURS VARCHAR(255),
  FK_TEACHING VARCHAR(255),
  DAY_WEEK VARCHAR(255),
  PRIMARY KEY (HOURS, FK_TEACHING, DAY_WEEK)
);

CREATE TABLE TEACHING (
  TEACHING_CODE VARCHAR(255),
  FK_DEPARTMENT VARCHAR(255),
  PRIMARY KEY (TEACHING_CODE, FK_DEPARTMENT)
);

CREATE TABLE DEPARTMENT (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE CLASSROOM (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE SITE (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  CITY VARCHAR(255),
  PRIMARY KEY (CODE)
);

