CREATE TABLE COLLEGE (
  NUMBER VARCHAR(255),
  PRIMARY KEY (NUMBER)
);

CREATE TABLE REGION (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE ELECTED (
  CODE VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE ELECTION (
  ANNO VARCHAR(255),
  VOTERS VARCHAR(255),
  PRIMARY KEY (ANNO)
);

CREATE TABLE PERSONA (
  CODE VARCHAR(255),
  PRIMARY KEY (CODE)
);