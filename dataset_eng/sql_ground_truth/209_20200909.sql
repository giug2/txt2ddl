CREATE TABLE TEACHING (
  FRESHMAN VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (FRESHMAN)
);

CREATE TABLE TEACHING_PROVIDED (
  FK_COURSE_OF_STUDY_CODE VARCHAR(255),
  FK_DEPARTMENT_CODE VARCHAR(255),
  ACADEMIC_YEAR VARCHAR(255),
  PRIMARY KEY (FK_COURSE_OF_STUDY_CODE, FK_DEPARTMENT_CODE)
);

CREATE TABLE COURSE_OF_STUDY (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  FOUNDATION_YEAR VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE COORDINATOR (
  ID VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  ASSIGNMENT_START_DATE VARCHAR(255),
  END_DATE_OF_ASSIGNMENT VARCHAR(255),
  PRIMARY KEY (ID)
);

CREATE TABLE DEPARTMENT (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  SITE VARCHAR(255),
  CONTACT VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE CONTRIBUTION_FEE (
  FK_SEGNAMENTO_EROGATO_CORSO VARCHAR(255),
  FK_TEACHING_PROVIDED_DEPARTMENT VARCHAR(255),
  FK_INCOME_BAND_CODE VARCHAR(255),
  AMOUNT VARCHAR(255),
  PRIMARY KEY (FK_SEGNAMENTO_EROGATO_CORSO, FK_TEACHING_PROVIDED_DEPARTMENT, FK_INCOME_BAND_CODE)
);

CREATE TABLE INCOME_RANGE (
  CODE VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE EXEMPTION_TYPE (
  CODE VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (CODE)
);

