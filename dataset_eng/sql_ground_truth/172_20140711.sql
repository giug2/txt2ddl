  WAREHOUSE_CODE VARCHAR(255),
  ADDRESS VARCHAR(255),
  PRIMARY KEY (WAREHOUSE_CODE)
);

CREATE TABLE ATTENDANT (
  FRESHMAN VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (FRESHMAN)
);

CREATE TABLE LOTTO (
  CODELOTTO VARCHAR(255),
  PRODUCTION_DATA VARCHAR(255),
  PRIMARY KEY (CODELOTTO)
);

