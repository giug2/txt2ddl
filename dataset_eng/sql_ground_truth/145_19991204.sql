  CODE VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE BOOK (
  CODE VARCHAR(255),
  TITLE VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE TYPE (
  SIGLAGENERE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (SIGLAGENERE)
);

CREATE TABLE EDITION (
  FK_BOOK VARCHAR(255),
  FK_EDITOR VARCHAR(255),
  ANNO VARCHAR(255),
  PRIMARY KEY (FK_BOOK, FK_EDITOR)
);

CREATE TABLE NECKLACE (
  SIGLACOLLANA VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (SIGLACOLLANA)
);

