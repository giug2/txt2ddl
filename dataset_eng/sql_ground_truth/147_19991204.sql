CREATE TABLE CASA (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE NATION (
  ACRONYM_NATION VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (ACRONYM_NATION)
);

CREATE TABLE MODEL (
  NAME VARCHAR(255),
  FK_HOME VARCHAR(255),
  PRIMARY KEY (NAME, FK_HOME)
);

CREATE TABLE AUTO (
  TARGA VARCHAR(255),
  ANNO VARCHAR(255),
  PRIMARY KEY (TARGA)
);

CREATE TABLE PERSONA (
  TAX_ID_CODE VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (TAX_ID_CODE)
);

CREATE TABLE VERSION (
  CODEVERSION VARCHAR(255),
  FK_MODEL VARCHAR(255),
  DISPLACEMENT VARCHAR(255),
  PRIMARY KEY (CODEVERSION, FK_MODEL)
);

CREATE TABLE CATEGORY (
  CATEGORYCODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CATEGORYCODE)
);

