);

CREATE TABLE CITY (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE NATION (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

