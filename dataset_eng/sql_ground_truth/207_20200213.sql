CREATE TABLE EMPLOYEE (
  EMPLOYEE_ID VARCHAR(255),
  FK_TEAM VARCHAR(255),
  PRIMARY KEY (EMPLOYEE_ID, FK_TEAM)
);

CREATE TABLE TEAM (
  ID_TEAM VARCHAR(255),
  FK_DIVISION VARCHAR(255),
  PRIMARY KEY (ID_TEAM, FK_DIVISION)
);

CREATE TABLE DIVISION (
  DIVISION_ID VARCHAR(255),
  FK_COMPANY VARCHAR(255),
  PRIMARY KEY (DIVISION_ID, FK_COMPANY)
);

CREATE TABLE AGENCY (
  COMPANY_ID VARCHAR(255),
  PRIMARY KEY (COMPANY_ID)
);