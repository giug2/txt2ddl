CREATE TABLE FESTIVAL (
  ID_FESTIVAL VARCHAR(255),
  NAME VARCHAR(255),
  YEAREDITION VARCHAR(255),
  PRIMARY KEY (ID_FESTIVAL)
);

CREATE TABLE LOCATION (
  ID_LOCATION VARCHAR(255),
  CITYNAME VARCHAR(255),
  VILLAGE VARCHAR(255),
  PRIMARY KEY (ID_LOCATION)
);

CREATE TABLE ARTIST (
  ARTIST_ID VARCHAR(255),
  NAMEART VARCHAR(255),
  NOMEREALE VARCHAR(255),
  ANNOINIZIOCAREER VARCHAR(255),
  PRIMARY KEY (ARTIST_ID)
);

CREATE TABLE GENEREMUSICALE (
  ID_GENERE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (ID_GENERE)
);

