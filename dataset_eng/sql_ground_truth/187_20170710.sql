CREATE TABLE ARTIST (
  ARTIST_ID VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  BIRTH_DATE VARCHAR(255),
  PRIMARY KEY (ARTIST_ID)
);

CREATE TABLE STUDIO_PRODUCTION (
  ID_STUDIO VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (ID_STUDIO)
);

CREATE TABLE FILM (
  ISAN VARCHAR(255),
  TITLE VARCHAR(255),
  PRIMARY KEY (ISAN)
);

CREATE TABLE DISTRIBUTOR (
  DISTRIBUTOR_ID VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (DISTRIBUTOR_ID)
);

CREATE TABLE PHYSICAL_SUPPORT (
  COPY_CODE VARCHAR(255),
  ENTRY_DATE VARCHAR(255),
  PRIMARY KEY (COPY_CODE)
);

