CREATE TABLE INSTITUTION (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  ADDRESS VARCHAR(255),
  NATION VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE AUTHOR (
  CODE VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  E-MAIL VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE ITEM (
  CODE VARCHAR(255),
  TITLE VARCHAR(255),
  SUBTITLE VARCHAR(255),
  TEXT VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE PUBLICATION (
  NUMBER VARCHAR(255),
  FK_VOLUME VARCHAR(255),
  DATA VARCHAR(255),
  PRIMARY KEY (NUMBER, FK_VOLUME)
);

CREATE TABLE VOLUME (
  ANNO VARCHAR(255),
  TITLE VARCHAR(255),
  PRIMARY KEY (ANNO)
);