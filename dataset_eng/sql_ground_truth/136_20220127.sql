CREATE TABLE MANAGER (
  ID_MANAGER VARCHAR(255),
  MANAGER_NAME VARCHAR(255),
  PRIMARY KEY (ID_MANAGER)
);

CREATE TABLE DEPARTMENT (
  DEPARTMENT_ID VARCHAR(255),
  DEPARTMENT_NAME VARCHAR(255),
  PRIMARY KEY (DEPARTMENT_ID)
);

CREATE TABLE ASSISTANT (
  ASSISTANT_ID VARCHAR(255),
  ASSISTANT_NAME VARCHAR(255),
  PRIMARY KEY (ASSISTANT_ID)
);

CREATE TABLE SITE (
  ID_SEDE VARCHAR(255),
  HEADQUARTERS_ADDRESS VARCHAR(255),
  PRIMARY KEY (ID_SEDE)
);

CREATE TABLE REL_MANAGER_ASSISTANT_DEPARTMENT (
  FK_MANAGER VARCHAR(255),
  FK_ASSISTANT VARCHAR(255),
  FK_DEPARTMENT VARCHAR(255),
  PRIMARY KEY (FK_MANAGER, FK_ASSISTANT, FK_DEPARTMENT),
  FOREIGN KEY (FK_MANAGER) REFERENCES MANAGER,
  FOREIGN KEY (FK_ASSISTANT) REFERENCES ASSISTANT,
  FOREIGN KEY (FK_DEPARTMENT) REFERENCES DEPARTMENT
);