CREATE TABLE BOOK (
  ISBN VARCHAR(255),
  TITLE VARCHAR(255),
  COPIES_AVAILABLE VARCHAR(255),
  YEARPUBLICATION VARCHAR(255),
  PRIMARY KEY (ISBN)
);

CREATE TABLE TYPE (
  IDGENERE VARCHAR(255),
  NOMEGENERATE VARCHAR(255),
  PRIMARY KEY (IDGENERE)
);

CREATE TABLE PUBLISHER (
  IDEPUBLISHER VARCHAR(255),
  NOMEEDITOR VARCHAR(255),
  PRIMARY KEY (IDEPUBLISHER)
);

CREATE TABLE LOAN (
  LOANID VARCHAR(255),
  DATAPRESTITO VARCHAR(255),
  STATE VARCHAR(255),
  PRIMARY KEY (LOANID)
);

CREATE TABLE USER (
  CARD VARCHAR(255),
  FULLNAME VARCHAR(255),
  ADDRESS VARCHAR(255),
  PRIMARY KEY (CARD)
);