CREATE TABLE NURSE (
  NURSE_ID VARCHAR(255),
  NAME VARCHAR(255),
  SURNAME VARCHAR(255),
  PRIMARY KEY (NURSE_ID)
);

CREATE TABLE DOCTOR (
  DOCTOR_ID VARCHAR(255),
  NAME VARCHAR(255),
  SURNAME VARCHAR(255),
  PRIMARY KEY (DOCTOR_ID)
);

CREATE TABLE HOSPITAL (
  HOSPITAL_ID VARCHAR(255),
  ADDRESS VARCHAR(255),
  CITTA VARCHAR(255),
  PRIMARY KEY (HOSPITAL_ID)
);

CREATE TABLE DEPARTMENT (
  DEPARTMENT_ID VARCHAR(255),
  FK_HOSPITAL VARCHAR(255),
  DEPARTMENT_NAME VARCHAR(255),
  PIANO VARCHAR(255),
  PRIMARY KEY (DEPARTMENT_ID, FK_HOSPITAL)
);

CREATE TABLE REL_DOCTOR_NURSING_DEPARTMENT (
  FK_DOCTOR VARCHAR(255),
  FK_NURSING VARCHAR(255),
  FK_DEPARTMENT VARCHAR(255),
  PRIMARY KEY (FK_DOCTOR, FK_NURSING, FK_DEPARTMENT),
  FOREIGN KEY (FK_DOCTOR) REFERENCES DOCTOR,
  FOREIGN KEY (FK_NURSING) REFERENCES NURSING,
  FOREIGN KEY (FK_DEPARTMENT) REFERENCES DEPARTMENT
);