CREATE TABLE CASA (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE NATION (
  ACRONYM_NATION VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (ACRONYM_NATION)
);

CREATE TABLE MODEL (
  NAME VARCHAR(255),
  FK_HOME VARCHAR(255),
  CATEGORY VARCHAR(255),
  PRIMARY KEY (NAME, FK_HOME)
);

CREATE TABLE AUTO (
  TARGA VARCHAR(255),
  ANNO VARCHAR(255),
  PRIMARY KEY (TARGA)
);

CREATE TABLE PERSONA (
  TAX_ID_CODE VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (TAX_ID_CODE)
);

