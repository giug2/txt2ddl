  FK_COMPANY VARCHAR(255),
  FK_RAPPORTEUR VARCHAR(255),
  ROLE_EVENT VARCHAR(255),
  PRIMARY KEY (FK_COMPANY, FK_RAPPORTEUR, ROLE_EVENT)
);

