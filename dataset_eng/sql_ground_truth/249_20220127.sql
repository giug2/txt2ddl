CREATE TABLE PROFESSOR (
  PROFESSOR_ID VARCHAR(255),
  FK_DEPARTMENT VARCHAR(255),
  FK_CAMPUS VARCHAR(255),
  PRIMARY KEY (PROFESSOR_ID, FK_DEPARTMENT, FK_CAMPUS)
);

CREATE TABLE DEPARTMENT (
  DEPARTMENT_ID VARCHAR(255),
  PRIMARY KEY (DEPARTMENT_ID)
);

CREATE TABLE TUTOR (
  ID_TUTOR VARCHAR(255),
  FK_PROFESSOR VARCHAR(255),
  FK_CAMPUS VARCHAR(255),
  PRIMARY KEY (ID_TUTOR, FK_PROFESSOR, FK_CAMPUS)
);

CREATE TABLE CAMPUS (
  ID_CAMPUS VARCHAR(255),
  PRIMARY KEY (ID_CAMPUS)
);