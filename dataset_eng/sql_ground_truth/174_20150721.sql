  ID_TAX VARCHAR(255),
  DATAEMISSION VARCHAR(255),
  AMOUNT VARCHAR(255),
  PRIMARY KEY (ID_TAX)
);

CREATE TABLE PAYMENT (
  ID_PAGEMENT VARCHAR(255),
  DATAPAYMENT VARCHAR(255),
  AMOUNT VARCHAR(255),
  PRIMARY KEY (ID_PAGEMENT)
);

CREATE TABLE DEGREE_COURSE (
