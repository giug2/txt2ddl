CREATE TABLE MANAGER (
  MANAGER_ID VARCHAR(255),
  FK_OFFICE VARCHAR(255),
  FK_SEDE VARCHAR(255),
  PRIMARY KEY (MANAGER_ID, FK_OFFICE, FK_SEDE)
);

CREATE TABLE OFFICE (
  OFFICE_ID VARCHAR(255),
  PRIMARY KEY (OFFICE_ID)
);

CREATE TABLE ASSISTANT (
  ASSISTANT_ID VARCHAR(255),
  FK_LEADER VARCHAR(255),
  FK_SEDE VARCHAR(255),
  PRIMARY KEY (ASSISTANT_ID, FK_LEADER, FK_SEDE)
);

CREATE TABLE SITE (
  ID_SEDE VARCHAR(255),
  PRIMARY KEY (ID_SEDE)
);