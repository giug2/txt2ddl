CREATE TABLE CASA (
  NAME VARCHAR(255),
  ADDRESS VARCHAR(255),
  PRIMARY KEY (NAME)
);

CREATE TABLE MODEL (
  FK_HOME VARCHAR(255),
  NAME VARCHAR(255),
  ANNO VARCHAR(255),
  PRIMARY KEY (FK_HOME, NAME)
);

CREATE TABLE VERSION (
  FK_MODEL VARCHAR(255),
  NAME VARCHAR(255),
  PREZZO VARCHAR(255),
  PORTE VARCHAR(255),
  SPEED VARCHAR(255),
  PRIMARY KEY (FK_MODEL, NAME)
);

CREATE TABLE SEGMENT (
  CODE VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE MOTOR (
  CODE VARCHAR(255),
  DISPLACEMENT VARCHAR(255),
  CYLINDERS VARCHAR(255),
  POWER VARCHAR(255),
  PRIMARY KEY (CODE)
);

