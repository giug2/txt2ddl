  CF VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  DATE_OF_BIRTH VARCHAR(255),
  PRIMARY KEY (CF)
);

CREATE TABLE USER (
  NUMBER VARCHAR(255),
  ADDRESS VARCHAR(255),
  PRIMARY KEY (NUMBER)
);

CREATE TABLE BILL (
  CODE VARCHAR(255),
  DATA VARCHAR(255),
  AMOUNT VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE PAYMENT (
  CODE VARCHAR(255),
  DATA VARCHAR(255),
  AMOUNT VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE DISTRICT (
  PREFIX VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (PREFIX)
);

