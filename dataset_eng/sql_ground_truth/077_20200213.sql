CREATE TABLE PROFESSOR (
  PROFESSOR_ID VARCHAR(255),
  FK_SCHOOL VARCHAR(255),
  FK_DEPARTMENT VARCHAR(255),
  NAME VARCHAR(255),
  SURNAME VARCHAR(255),
  PRIMARY KEY (PROFESSOR_ID, FK_SCHOOL, FK_DEPARTMENT)
);

CREATE TABLE COURSE_OF_STUDY (
  STUDY_COURSE_ID VARCHAR(255),
  FK_SCHOOL VARCHAR(255),
  COURSE_NAME VARCHAR(255),
  PRIMARY KEY (STUDY_COURSE_ID, FK_SCHOOL)
);

CREATE TABLE DEPARTMENT (
  DEPARTMENT_ID VARCHAR(255),
  DEPARTMENT_NAME VARCHAR(255),
  PRIMARY KEY (DEPARTMENT_ID)
);

CREATE TABLE SCHOOL (
  SCHOOL_ID VARCHAR(255),
  SCHOOL_NAME VARCHAR(255),
  PRIMARY KEY (SCHOOL_ID)
);