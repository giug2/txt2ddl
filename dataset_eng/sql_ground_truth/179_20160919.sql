CREATE TABLE COPY_BOOK (
  ID_COPY VARCHAR(255),
  CONSERVATION_STATUS VARCHAR(255),
  PURCHASE_DATE VARCHAR(255),
  PRIMARY KEY (ID_COPY)
);

CREATE TABLE EDITION (
  ISBN VARCHAR(255),
  TITLE VARCHAR(255),
  NUMBER_PAGES VARCHAR(255),
  PREZZO VARCHAR(255),
  PRIMARY KEY (ISBN)
);

CREATE TABLE FORMAT (
  FORMAT_ID VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (FORMAT_ID)
);

CREATE TABLE BOOK (
  BOOK_ID VARCHAR(255),
  ORIGINAL_TITLE VARCHAR(255),
  PUBLICATION_YEAR VARCHAR(255),
  AUTHOR VARCHAR(255),
  PRIMARY KEY (BOOK_ID)
);

CREATE TABLE LITERARY_GENRE (
  ID_GENERATE VARCHAR(255),
  GENRE_NAME VARCHAR(255),
  PRIMARY KEY (ID_GENERATE)
);

CREATE TABLE PUBLISHING_HOUSE (
  PUBLISHER_ID VARCHAR(255),
  PUBLISHER_NAME VARCHAR(255),
