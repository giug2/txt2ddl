CREATE TABLE PRODUCT (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  QUANTITY_AVAILABLE VARCHAR(255),
  PREZZO VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE CATEGORY (
  CODE VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE SUPPLIER (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE ORDER (
  NUMBER VARCHAR(255),
  DATA VARCHAR(255),
  STATE VARCHAR(255),
  PRIMARY KEY (NUMBER)
);

CREATE TABLE CUSTOMER (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  ADDRESS VARCHAR(255),
  PRIMARY KEY (CODE)
);