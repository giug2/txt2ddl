CREATE TABLE DEVELOPER (
  DEVELOPER_ID VARCHAR(255),
  FK_COMPANY VARCHAR(255),
  FK_TEAM VARCHAR(255),
  NAME VARCHAR(255),
  SURNAME VARCHAR(255),
  PRIMARY KEY (DEVELOPER_ID, FK_COMPANY, FK_TEAM)
);

CREATE TABLE PROJECT (
  PROJECT_ID VARCHAR(255),
  FK_COMPANY VARCHAR(255),
  PROJECT_NAME VARCHAR(255),
  PRIMARY KEY (PROJECT_ID, FK_COMPANY)
);

CREATE TABLE TEAM (
  ID_TEAM VARCHAR(255),
  TEAM_NAME VARCHAR(255),
  PRIMARY KEY (ID_TEAM)
);

CREATE TABLE AGENCY (
  COMPANY_ID VARCHAR(255),
  COMPANY_NAME VARCHAR(255),
  PRIMARY KEY (COMPANY_ID)
);