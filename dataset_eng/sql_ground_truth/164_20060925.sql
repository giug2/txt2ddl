  CODE VARCHAR(255),
  NAME VARCHAR(255),
  DATEINITIATION VARCHAR(255),
  DATAFINE VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE THEMATIC_PATH (
  CODE VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE PERFORMANCE (
  FK_FESTIVAL VARCHAR(255),
  FK_ARTISTA VARCHAR(255),
  PRIMARY KEY (FK_FESTIVAL, FK_ARTISTA)
);

CREATE TABLE ARTIST (
  CODE VARCHAR(255),
  GENEREMUSICALE VARCHAR(255),
  TRAINING_YEAR VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE STAGE (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  CAPACITY VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE TOURGUIDATO (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  DEPARTURE_TIME VARCHAR(255),
  TERM VARCHAR(255),
  PRIMARY KEY (CODE)
);

