  TARGA VARCHAR(255),
  SEGMENT VARCHAR(255),
  NUMBER_OF_PLACES VARCHAR(255),
  REGISTRATION_YEAR VARCHAR(255),
  MOTORIZATION VARCHAR(255),
  PRIMARY KEY (PK_VEHICLE)
);

