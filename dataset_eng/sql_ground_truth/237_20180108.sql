CREATE TABLE DEGREE_COURSE (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  TIPOLAUREA VARCHAR(255),
  YEARACTIVATION VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE DEPARTMENT (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE FACULTY (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE STUDENTS (
  FRESHMAN VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  MEDIAVOTI VARCHAR(255),
  CFU_ACQUIRED VARCHAR(255),
  PRIMARY KEY (FRESHMAN)
);

CREATE TABLE STATE_STUDENT (
  CODE VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (CODE)
);

