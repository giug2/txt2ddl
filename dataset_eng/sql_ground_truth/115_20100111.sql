  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE CONCERTO (
  CODE VARCHAR(255),
  TITLE VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (CODE, TITLE)
);

