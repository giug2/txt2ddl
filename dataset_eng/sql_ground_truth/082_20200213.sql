CREATE TABLE TEACHING (
  TEACHER_ID VARCHAR(255),
  FK_COURSE VARCHAR(255),
  PRIMARY KEY (TEACHER_ID, FK_COURSE)
);

CREATE TABLE COURSE_OF_STUDY (
  STUDY_COURSE_ID VARCHAR(255),
  FK_DEPARTMENT VARCHAR(255),
  PRIMARY KEY (STUDY_COURSE_ID, FK_DEPARTMENT)
);

CREATE TABLE DEPARTMENT (
  DEPARTMENT_ID VARCHAR(255),
  FK_FACOLTA VARCHAR(255),
  PRIMARY KEY (DEPARTMENT_ID, FK_FACOLTA)
);

CREATE TABLE FACULTY (
  FACULTY_ID VARCHAR(255),
  PRIMARY KEY (FACULTY_ID)
);