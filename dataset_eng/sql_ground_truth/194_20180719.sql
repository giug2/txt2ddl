CREATE TABLE UNIVERSITY (
  ID VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (ID)
);

CREATE TABLE CITTA (
  ID VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (ID)
);

CREATE TABLE REGION (
  SAILING VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (SAILING)
);

CREATE TABLE DEPARTMENT (
  NAME VARCHAR(255),
  FK_UNIVERSITA VARCHAR(255),
  NUMBER_OFFICES VARCHAR(255),
  PRIMARY KEY (NAME, FK_UNIVERSITA)
);

CREATE TABLE CORSO (
  CODE VARCHAR(255),
  COURSE_NAME VARCHAR(255),
  ACADEMIC_YEAR VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE PROFESSOR (
  SERIAL_NUMBER VARCHAR(255),
  NAME VARCHAR(255),
  SURNAME VARCHAR(255),
  PRIMARY KEY (SERIAL_NUMBER)
);

