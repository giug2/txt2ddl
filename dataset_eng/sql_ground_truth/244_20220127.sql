CREATE TABLE ARTIST (
  ARTIST_ID VARCHAR(255),
  PRIMARY KEY (ARTIST_ID)
);

CREATE TABLE ALBUM (
  ID_ALBUM VARCHAR(255),
  FK_GENERA VARCHAR(255),
  PRIMARY KEY (ID_ALBUM, FK_GENERA)
);

CREATE TABLE SONG (
  SONG_ID VARCHAR(255),
  FK_ALBUM VARCHAR(255),
  PRIMARY KEY (SONG_ID, FK_ALBUM)
);

CREATE TABLE TYPE (
  ID_TYPE VARCHAR(255),
  PRIMARY KEY (ID_TYPE)
);