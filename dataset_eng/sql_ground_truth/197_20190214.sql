  TEACHER_ID VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (TEACHER_ID)
);

CREATE TABLE DEPARTMENT (
  COD_DIP VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (COD_DIP)
);

CREATE TABLE LESSON (
  LESSON_NUMBER VARCHAR(255),
  FK_COURSE VARCHAR(255),
  PRIMARY KEY (LESSON_NUMBER, FK_COURSE)
);

