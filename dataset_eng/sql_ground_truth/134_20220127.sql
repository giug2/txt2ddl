CREATE TABLE ASSISTANT (
  ASSISTANT_ID VARCHAR(255),
  NAME VARCHAR(255),
  SURNAME VARCHAR(255),
  PRIMARY KEY (ASSISTANT_ID)
);

CREATE TABLE MANAGER (
  MANAGER_ID VARCHAR(255),
  NAME VARCHAR(255),
  SURNAME VARCHAR(255),
  PRIMARY KEY (MANAGER_ID)
);

CREATE TABLE SITE (
  ID_SEDE VARCHAR(255),
  ADDRESS VARCHAR(255),
  CITTA VARCHAR(255),
  PRIMARY KEY (ID_SEDE)
);

CREATE TABLE OFFICE (
  OFFICE_ID VARCHAR(255),
  FK_SEDE VARCHAR(255),
  OFFICE_NAME VARCHAR(255),
  PIANO VARCHAR(255),
  PRIMARY KEY (OFFICE_ID, FK_SEDE)
);

CREATE TABLE REL_MANAGER_ASSISTANT_OFFICE (
  FK_MANAGER VARCHAR(255),
  FK_ASSISTANT VARCHAR(255),
  FK_OFFICE VARCHAR(255),
  PRIMARY KEY (FK_MANAGER, FK_ASSISTANT, FK_OFFICE),
  FOREIGN KEY (FK_MANAGER) REFERENCES MANAGER,
  FOREIGN KEY (FK_ASSISTANT) REFERENCES ASSISTANT,
  FOREIGN KEY (FK_OFFICE) REFERENCES OFFICE
);