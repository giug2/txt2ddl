CREATE TABLE PROFESSOR (
  PROFESSOR_ID VARCHAR(255),
  PRIMARY KEY (PROFESSOR_ID)
);

CREATE TABLE CORSO (
  COURSE_ID VARCHAR(255),
  FK_DEPARTMENT VARCHAR(255),
  PRIMARY KEY (COURSE_ID, FK_DEPARTMENT)
);

CREATE TABLE TUTOR (
  ID_TUTOR VARCHAR(255),
  PRIMARY KEY (ID_TUTOR)
);

CREATE TABLE DEPARTMENT (
  DEPARTMENT_ID VARCHAR(255),
  PRIMARY KEY (DEPARTMENT_ID)
);

CREATE TABLE REL_PROFESSOR_TUTOR_CORSO (
  FK_PROFESSOR VARCHAR(255),
  FK_TUTOR VARCHAR(255),
  FK_CORSO VARCHAR(255),
  PRIMARY KEY (FK_PROFESSOR, FK_TUTOR, FK_CORSO),
  FOREIGN KEY (FK_PROFESSOR) REFERENCES PROFESSOR,
  FOREIGN KEY (FK_TUTOR) REFERENCES TUTOR,
  FOREIGN KEY (FK_CORSO) REFERENCES CORSO
);