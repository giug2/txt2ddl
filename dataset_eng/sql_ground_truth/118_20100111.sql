);

CREATE TABLE MUSICIAN (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE EXECUTION (
  POSITION VARCHAR(255),
  FK_CONCERTO VARCHAR(255),
  PRIMARY KEY (POSITION, FK_CONCERTO)
);

CREATE TABLE CONCERTO (
  CODE VARCHAR(255),
  TITLE VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (CODE)
);

CREATE TABLE REPLICA (
  DATA VARCHAR(255),
  FK_CONCERTO VARCHAR(255),
  PRIMARY KEY (DATA, FK_CONCERTO)
);

