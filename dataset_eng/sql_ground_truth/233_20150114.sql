CREATE TABLE RACK (
  SHELFCODE VARCHAR(255),
  PRIMARY KEY (SHELFCODE)
);

CREATE TABLE WAREHOUSE (
  WAREHOUSE_CODE VARCHAR(255),
  ADDRESS VARCHAR(255),
  PRIMARY KEY (WAREHOUSE_CODE)
);

CREATE TABLE PRODUCT (
  SKU VARCHAR(255),
  PRIMARY KEY (SKU)
);

CREATE TABLE INVENTORY (
  DATAINVENTARIO VARCHAR(255),
  COUNTED_ITEMS VARCHAR(255),
  PRIMARY KEY (DATAINVENTARIO)
);

CREATE TABLE OPERATOR (
  FRESHMAN VARCHAR(255),
  PRIMARY KEY (FRESHMAN)
);