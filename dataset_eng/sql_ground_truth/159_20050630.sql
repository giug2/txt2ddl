CREATE TABLE COURSE_SESSION (
  COURSE_SESSION_ID VARCHAR(255),
  DATA_FINE VARCHAR(255),
  START_DATE VARCHAR(255),
  PRIMARY KEY (COURSE_SESSION_ID)
);

CREATE TABLE CORSO (
  COURSE_ID VARCHAR(255),
  TITLE VARCHAR(255),
  PUBLICATION_YEAR VARCHAR(255),
  PRIMARY KEY (COURSE_ID)
);

CREATE TABLE TEACHING (
  TEACHER_ID VARCHAR(255),
  NAME VARCHAR(255),
  SURNAME VARCHAR(255),
  DATE_OF_BIRTH VARCHAR(255),
  COLLECTION_DATE VARCHAR(255),
  PRIMARY KEY (TEACHER_ID)
);

CREATE TABLE E-LEARNING_PLATFORM (
  PLATFORM_ID VARCHAR(255),
  NAME VARCHAR(255),
  SITE_URL VARCHAR(255),
  SUPPORT_CONTACT VARCHAR(255),
  PRIMARY KEY (PLATFORM_ID)
);

CREATE TABLE DISCOUNT_TYPE (
  DISCOUNT_TYPE_ID VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (DISCOUNT_TYPE_ID)
);

CREATE TABLE RATE (
  ID_TARIFF VARCHAR(255),
  PRIMARY KEY (ID_TARIFF)
);

CREATE TABLE ACCESS_LEVEL (
  ACCESS_LEVEL_ID VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (ACCESS_LEVEL_ID)
);

CREATE TABLE REGISTRATION_TYPE (
  REGISTRATION_TYPE_ID VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (REGISTRATION_TYPE_ID)
);

