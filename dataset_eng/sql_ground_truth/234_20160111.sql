CREATE TABLE COPY_WAREHOUSE (
  ID_COPY VARCHAR(255),
  CONSERVATION_STATUS VARCHAR(255),
  ACQUISITION_DATE VARCHAR(255),
  PRIMARY KEY (ID_COPY)
);

CREATE TABLE EDITION (
  ISBN VARCHAR(255),
  TITLE_EDITION VARCHAR(255),
  FORMAT VARCHAR(255),
  COVER_PRICE VARCHAR(255),
  PRIMARY KEY (ISBN)
);

CREATE TABLE LINGUA (
  ISO_CODE VARCHAR(255),
  LANGUAGE_NAME VARCHAR(255),
  PRIMARY KEY (ISO_CODE)
);

CREATE TABLE LITERARY_WORK (
  ID_OPERA VARCHAR(255),
  AVERAGE_NUMBER_PAGES VARCHAR(255),
  NUMBER_VOLUMES VARCHAR(255),
  PRIMARY KEY (ID_OPERA)
);

CREATE TABLE TYPE (
  ID_GENERATE VARCHAR(255),
  GENRE_NAME VARCHAR(255),
  PRIMARY KEY (ID_GENERATE)
);

