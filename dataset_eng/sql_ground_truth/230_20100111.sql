  FILMCODE VARCHAR(255),
  TITLE VARCHAR(255),
  PRIMARY KEY (FILMCODE)
);

CREATE TABLE DIRECTOR (
  DIRECTORCODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (DIRECTORCODE)
);

CREATE TABLE EVENT (
  EVENTCODE VARCHAR(255),
  TITLE VARCHAR(255),
  DESCRIPTION VARCHAR(255),
  PRIMARY KEY (EVENTCODE)
);

