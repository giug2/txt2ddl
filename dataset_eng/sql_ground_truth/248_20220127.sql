CREATE TABLE PROFESSOR (
  PROFESSOR_ID VARCHAR(255),
  PRIMARY KEY (PROFESSOR_ID)
);

CREATE TABLE DEPARTMENT (
  DEPARTMENT_ID VARCHAR(255),
  FK_CAMPUS VARCHAR(255),
  PRIMARY KEY (DEPARTMENT_ID, FK_CAMPUS)
);

CREATE TABLE STUDENTS (
  ID_STUDENTS VARCHAR(255),
  FK_DEPARTMENT VARCHAR(255),
  PRIMARY KEY (ID_STUDENTS, FK_DEPARTMENT)
);

CREATE TABLE CAMPUS (
  ID_CAMPUS VARCHAR(255),
  PRIMARY KEY (ID_CAMPUS)
);