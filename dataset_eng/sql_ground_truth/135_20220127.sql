CREATE TABLE MANAGER (
  ID_MANAGER VARCHAR(255),
  FK_DEPARTMENT VARCHAR(255),
  FK_SEDE VARCHAR(255),
  PRIMARY KEY (ID_MANAGER, FK_DEPARTMENT, FK_SEDE)
);

CREATE TABLE DEPARTMENT (
  DEPARTMENT_ID VARCHAR(255),
  PRIMARY KEY (DEPARTMENT_ID)
);

CREATE TABLE SITE (
  ID_SEDE VARCHAR(255),
  PRIMARY KEY (ID_SEDE)
);

CREATE TABLE ASSISTANT (
  ASSISTANT_ID VARCHAR(255),
  FK_MANAGER VARCHAR(255),
  FK_SEDE VARCHAR(255),
  PRIMARY KEY (ASSISTANT_ID, FK_MANAGER, FK_SEDE)
);