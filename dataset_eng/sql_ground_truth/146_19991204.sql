CREATE TABLE PERSONA (
  TAX_ID_CODE VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (TAX_ID_CODE)
);

CREATE TABLE PURCHASE (
  DATA VARCHAR(255),
  FK_AUTO VARCHAR(255),
  PRIMARY KEY (DATA, FK_AUTO)
);

CREATE TABLE AUTO (
  TARGA VARCHAR(255),
  ANNO VARCHAR(255),
  PRIMARY KEY (TARGA)
);

CREATE TABLE VERSION (
  CODE VARCHAR(255),
  FK_MODEL VARCHAR(255),
  DISPLACEMENT VARCHAR(255),
  PRIMARY KEY (CODE, FK_MODEL)
);

CREATE TABLE MODEL (
  NAME VARCHAR(255),
  FK_HOME VARCHAR(255),
  CATEGORY VARCHAR(255),
  PRIMARY KEY (NAME, FK_HOME)
);

CREATE TABLE CASA (
  CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CODE)
);

