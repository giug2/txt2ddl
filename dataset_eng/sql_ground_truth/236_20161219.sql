CREATE TABLE UNIVERSITY (
  UNIVERSITY_CODE VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (UNIVERSITY_CODE)
);

CREATE TABLE REGION (
  ID VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (ID)
);

CREATE TABLE STATE (
  SAILING VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (SAILING)
);

CREATE TABLE DEPARTMENT (
  NAME VARCHAR(255),
  FK_UNIVERSITA VARCHAR(255),
  CLASSROOM_NUMBER VARCHAR(255),
  PRIMARY KEY (NAME, FK_UNIVERSITA)
);

CREATE TABLE DEGREE_COURSE (
  COURSE_CODE VARCHAR(255),
  COURSE_NAME VARCHAR(255),
  YEAR_INSTITUTION VARCHAR(255),
  PRIMARY KEY (COURSE_CODE)
);

CREATE TABLE TEACHING (
  SERIAL_NUMBER VARCHAR(255),
  PRIMARY KEY (SERIAL_NUMBER)
);

