CREATE TABLE CUSTOMER (
  CUSTOMER_CODE VARCHAR(255),
  SURNAME VARCHAR(255),
  NAME VARCHAR(255),
  PRIMARY KEY (CUSTOMER_CODE)
);

CREATE TABLE ORDER (
  ORDERID VARCHAR(255),
  DATAORDINE VARCHAR(255),
  PRIMARY KEY (ORDERID)
);

CREATE TABLE RIGAORDINE (
  FK_ORDER VARCHAR(255),
  ROWNUMBER VARCHAR(255),
  PRODUCT_DESCRIPTION VARCHAR(255),
  DELIVERY_DATE VARCHAR(255),
  PREZZO VARCHAR(255),
  SUPPLIER VARCHAR(255),
  PRIMARY KEY (FK_ORDER, ROWNUMBER)
);

