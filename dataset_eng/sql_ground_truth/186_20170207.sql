CREATE TABLE TEACHING (
  TEACHER_ID VARCHAR(255),
  PRIMARY KEY (TEACHER_ID)
);

CREATE TABLE STUDENTS (
  STUDENT_MATRICULATION VARCHAR(255),
  FK_CORSO_LAUREA VARCHAR(255),
  PRIMARY KEY (STUDENT_MATRICULATION, FK_CORSO_LAUREA)
);

CREATE TABLE EXAMINATION (
  ID_EXAM VARCHAR(255),
  FK_TEACHER VARCHAR(255),
  PRIMARY KEY (ID_EXAM, FK_TEACHER)
);

CREATE TABLE DEGREE_COURSE (
  DEGREE_COURSE_ID VARCHAR(255),
  PRIMARY KEY (DEGREE_COURSE_ID)
);